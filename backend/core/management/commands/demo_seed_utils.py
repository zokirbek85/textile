def select_invoice_payment_status(amount_due: Decimal, paid: Decimal) -> str:
    if paid >= amount_due:
        return "paid"
    if paid > Decimal("0"):
        return "partially_paid"
    return "issued"


def select_batch_status(batch_date: date, today: date) -> str:
    if batch_date < today - timedelta(days=14):
        return "completed"
    if batch_date < today - timedelta(days=3):
        return "in_progress"
    return "draft"


def random_choice_weighted(choices: Sequence[tuple[str, float]]) -> str:
    names, weights = zip(*choices)
    return random.choices(names, weights=weights, k=1)[0]
