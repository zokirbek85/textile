def choose_contract_share() -> tuple[Decimal, Decimal, Decimal]:
    processor = Decimal(random.choice([50, 55, 60, 65, 70]))
    customer = Decimal(random.choice([25, 30, 35, 40]))
    loss = Decimal("100") - processor - customer
    if loss < 0:
        processor = Decimal("60")
        customer = Decimal("35")
        loss = Decimal("5")
    return processor, customer, loss


