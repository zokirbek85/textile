def clamp_decimal(value: Decimal, min_value: Decimal, max_value: Decimal) -> Decimal:
    if value < min_value:
        return min_value
    if value > max_value:
        return max_value
    return value


def generate_invoice_amount(base_amount: Decimal, delivered_qty: Decimal, total_qty: Decimal) -> Decimal:
    if not total_qty or total_qty == Decimal("0"):
        return Decimal("0")
    share = delivered_qty / total_qty
    return round_money(base_amount * share)


def split_amount(total: Decimal, parts: int) -> list[Decimal]:
    if parts <= 1:
        return [total]
    base = (total / parts).quantize(Decimal("0.0001"), rounding=ROUND_HALF_UP)
    result = [base] * parts
    remainder = total - (base * parts)
    for idx in range(len(result)):
        if remainder == Decimal("0"):
            break
        result[idx] += Decimal("0.0001")
        remainder -= Decimal("0.0001")
    return result


