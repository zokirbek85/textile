def seed_rng(seed: Optional[int] = None) -> int:
    if seed is None:
        seed = random.randint(1_000_000, 9_999_999)
    random.seed(seed)
    faker.seed_instance(seed)
    return seed


def decimal(value: float | str | Decimal, places: int = 4) -> Decimal:
    quantize = Decimal("1") if places <= 0 else Decimal("1").scaleb(-places)
    return Decimal(str(value)).quantize(quantize, rounding=ROUND_HALF_UP)


def decimal_weight(value: float | str | Decimal) -> Decimal:
    return decimal(value, places=3)


def decimal_money(value: float | str | Decimal) -> Decimal:
    return decimal(value, places=4)


def business_day_rate(date_value: date, base_rate: Decimal = Decimal("11500")) -> Decimal:
    day_offset = (date_value - date_value.replace(day=1)).days
    drift = Decimal("0.0006") * Decimal(day_offset)
    noise = Decimal(str(random.uniform(-0.5, 0.5))) * Decimal("15")
    rate = base_rate + drift + noise
    return rate.quantize(Decimal("0.0001"), rounding=ROUND_HALF_UP)


def build_usd_uzs_series(start_date: date, days: int) -> list[Tuple[date, Decimal]]:
    rates = []
    current = start_date
    base_rate = Decimal("11500") + Decimal(str(random.uniform(-80, 80)))
    for _ in range(days):
        if current.weekday() >= 5:
            change = Decimal(str(random.uniform(-5, 5)))
        else:
            change = Decimal(str(random.uniform(-25, 25)))
        base_rate = max(10400, base_rate + change)
        rates.append((current, base_rate.quantize(Decimal("0.0001"), rounding=ROUND_HALF_UP)))
        current += timedelta(days=1)
    return rates


